window.LANG = localStorage.getItem('bb_lang') || 'ar';

window.t = function (key) {
  const d = window.DICT[window.LANG] || window.DICT.en;
  return (key in d) ? d[key] : (window.DICT.en[key] !== undefined ? window.DICT.en[key] : key);
};

/* Localized value from a {en, ar} pair */
window.tv = function (pair) {
  if (pair == null) return '';
  if (typeof pair === 'string') return pair;
  return pair[window.LANG] || pair.en || '';
};
